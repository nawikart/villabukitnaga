        // activate mansonry grid

        let masonry = new Masonry(this.selector, this.options);

        this.$emit("masonry-loaded", masonry);
      });
    }
  },
  watch: {
    data() {
      this.loaded();
    }
  },

  mounted() {
    this.loaded();
  }
